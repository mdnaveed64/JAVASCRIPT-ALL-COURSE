// Sqrt
let sqrt = Math.sqrt(144);
console.log(`Sqrt of 144 is : ${sqrt}`);

// pow
let pow = Math.pow(2,5);
console.log(`2 ^ of 5 is : ${pow}`);

// min
let min = Math.min(12,45,78,93,62,51,8,46,3,654,891,1,19,81);
console.log(`Min is : ${min}`);

// max
let max = Math.max(12,45,78,93,62,51,8,46,3,654,891,1,19,81);
console.log(`Max is : ${max}`);

// random , round
let random = Math.round(Math.random() * 10000);
